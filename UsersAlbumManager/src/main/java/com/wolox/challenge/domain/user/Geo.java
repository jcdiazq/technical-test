package com.wolox.challenge.domain.user;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class Geo {
	
public Geo() {
	}

private float lat;
private float lng;

public float getLat() {
	return lat;
}
public void setLat(float lat) {
	this.lat = lat;
}
public float getLng() {
	return lng;
}
public void setLng(float lng) {
	this.lng = lng;
}

}
